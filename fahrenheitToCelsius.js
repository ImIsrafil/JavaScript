var fahrenheit = 100;
var celsius;

celsius = ((fahrenheit - 32) * (5/9)).toFixed(2);

console.log(fahrenheit + " fahrenheit is " + celsius + " celsius");


// celsius to fahrenheit

var celsius = 10;
var fahrenheit;

